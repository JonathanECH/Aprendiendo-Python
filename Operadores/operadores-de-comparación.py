#Igual que (==)
igual_que = 5 == 5

#distinto que (!=)
distinto_que = 5 != 3

#mayor que (>)
mayor_que = 5 > 3

#menor que (<)
menor_que = 5 < 3

#mayor o igual que (>=)
mayor_o_igual_que = 5 >= 5

#menor o igual que (<=)
menor_o_igual_que = 5 <= 3

print(f"Igual que: {igual_que}")
print(f"Distinto que: {distinto_que}")
print(f"Mayor que: {mayor_que}")
print(f"Menor que: {menor_que}")
print(f"Mayor o igual que: {mayor_o_igual_que}")
print(f"Menor o igual que: {menor_o_igual_que}")